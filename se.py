# Write a program to find the intersection of two sets. Set1 = {green, blue} Set2 = {blue, yellow}
Set1 = {"green", "blue"} 
Set2 = {"blue", "yellow"}
# WHAT IS AN INTERSECTION IT IS A VEFY COMMONA VALUE BETEWEEN SET 1 AND SET TWOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOO
intersection = Set1.intersection(Set2)
print(intersection)
union = Set1.union(Set2)
print(union)
different = Set1.difference(Set2)
print(different)